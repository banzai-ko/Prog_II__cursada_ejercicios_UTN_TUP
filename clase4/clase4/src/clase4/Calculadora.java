public class Calculadora {
    
    public static double sumar( int a, int b) {
        return (double ) a + b;
    }
    
    public static int sumar(int a, int b, int c) {
        return a + b + c;
    }
    
    public static String sumar (String a, String b) {
        return a + "  " + b;
    }

}
